import java.util.*;
public class Stringtoint {
    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        String s=sc.nextLine();
        int b=Integer.parseInt(s);
        System.out.print(b);
    }
    
}
